CREATE DATABASE university_data;
USE university_data;

CREATE TABLE users (
    user_id INT PRIMARY KEY AUTO_INCREMENT,
    user_type ENUM('student', 'instructor') NOT NULL,
    reference_id INT,
    password VARCHAR(255) NOT NULL,
    email VARCHAR(100) NOT NULL,
    FOREIGN KEY (reference_id) REFERENCES students(user_id),
    FOREIGN KEY (reference_id) REFERENCES instructors(instructor_id)
);

CREATE TABLE courses (
    course_id VARCHAR(10) PRIMARY KEY,
    course_name VARCHAR(100) NOT NULL,
    course_title VARCHAR(100),
    course_description TEXT,
    prerequisites VARCHAR(100),
    instructor_name VARCHAR(100)
);


CREATE TABLE students (
    user_id INT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    course_id VARCHAR(10),
    query_history TEXT,
    FOREIGN KEY (course_id) REFERENCES courses(course_id)
);

CREATE TABLE instructors (
    instructor_id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL
);


