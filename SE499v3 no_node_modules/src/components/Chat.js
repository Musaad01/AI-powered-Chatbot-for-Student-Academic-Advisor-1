import React, { useState } from 'react';
import axios from 'axios';

function Chat() {
  const [prompt, setPrompt] = useState('');
  const [response, setResponse] = useState('');
  const [isLoading, setIsLoading] = useState(false);

  const handleSend = async (e) => {
    e.preventDefault();
    setIsLoading(true);

    try {
      console.log('Sending request to backend with prompt:', prompt);
      const res = await axios.post('http://localhost:3100/generate-text', { prompt });
      console.log('Full response:', res);
      
      if (res.data && res.data.generatedText) {
        setResponse(res.data.generatedText);
      } else {
        console.error('Unexpected response structure:', res.data);
        setResponse('Received an unexpected response from the server.');
      }
    } catch (error) {
      console.error('Error object:', error);
      if (error.response) {
        console.error('Error data:', error.response.data);
        console.error('Error status:', error.response.status);
        console.error('Error headers:', error.response.headers);
      } else if (error.request) {
        console.error('No response received:', error.request);
      } else {
        console.error('Error message:', error.message);
      }
      setResponse('An error occurred while generating a response. Please check the console for more details.');
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="chat">
      <h2>Chat with Bot</h2>
      <div className="chat-messages">
        {response && (
          <div className="message">
            <strong>Bot:</strong> {response}
          </div>
        )}
      </div>
      <form onSubmit={handleSend}>
        <input
          type="text"
          placeholder="Type your message"
          value={prompt}
          onChange={(e) => setPrompt(e.target.value)}
          required
        />
        <button type="submit" disabled={isLoading}>
          {isLoading ? 'Sending...' : 'Send'}
        </button>
      </form>
    </div>
  );
}

export default Chat;
